//! Linux dirent64 helpers

use std::cmp::Ordering;
use std::ffi::CString;
use std::ptr;

use serde::Deserialize;
use serde::Serialize;

#[derive(Debug, Clone, PartialEq, Eq, Deserialize, Serialize)]
pub struct Dirent64 {
    pub(crate) ino: u64,
    pub(crate) off: i64,
    pub(crate) ty: u8,
    pub(crate) reclen: u16,
    /// null terminated c string, NB: multiple null bytes are expected!
    pub(crate) name: CString,
}

// sort by name, but "." < ".." <= ..
impl PartialOrd for Dirent64 {
    fn partial_cmp(&self, other: &Self) -> Option<Ordering> {
        if self.name.as_bytes()[..2] == b".\0"[..] {
            Some(Ordering::Less)
        } else if other.name.as_bytes()[..2] == b".\0"[..] {
            Some(Ordering::Greater)
        } else if self.name.as_bytes()[..3] == b"..\0"[..] {
            Some(Ordering::Less)
        } else if other.name.as_bytes()[..3] == b"..\0"[..] {
            Some(Ordering::Greater)
        } else {
            self.name.partial_cmp(&other.name)
        }
    }
}

impl Ord for Dirent64 {
    fn cmp(&self, other: &Self) -> Ordering {
        self.partial_cmp(&other).unwrap()
    }
}

pub unsafe fn deserialize_dirents64(bytes: &[u8]) -> Vec<Dirent64> {
    let mut res = Vec::new();

    let dirp: *const u8 = bytes.as_ptr();
    let nb = bytes.len();
    let mut k = 0;

    while k < nb as isize {
        let dirp = dirp.offset(k);
        let mut j: isize = 0;
        let ino = ptr::read(dirp.offset(j).cast::<u64>());
        j += std::mem::size_of::<u64>() as isize;
        let off = ptr::read(dirp.offset(j).cast::<i64>());
        j += std::mem::size_of::<i64>() as isize;
        let reclen = ptr::read(dirp.offset(j).cast::<u16>());
        j += std::mem::size_of::<u16>() as isize;
        let ty = ptr::read(dirp.offset(j).cast::<u8>());
        j += std::mem::size_of::<u8>() as isize;
        let name = std::slice::from_raw_parts(dirp.offset(j), 1 + reclen as usize - j as usize);
        let name = Vec::from(name);
        let ent = Dirent64 {
            ino,
            off,
            reclen,
            ty,
            name: CString::from_vec_with_nul_unchecked(name),
        };
        res.push(ent);
        k += reclen as isize;
    }
    res
}

pub unsafe fn serialize_dirents64(dents: &[Dirent64], bytes: &mut [u8]) -> usize {
    let nb = bytes.len();
    let mut k = 0;
    let mut i = 0;

    while k < nb as isize && i < dents.len() {
        let ent = &dents[i];
        let dirp = bytes.as_mut_ptr().offset(k);
        let mut j: isize = 0;
        ptr::write(dirp.offset(j).cast::<u64>(), ent.ino);
        j += std::mem::size_of::<u64>() as isize;
        ptr::write(dirp.offset(j).cast::<i64>(), ent.off);
        j += std::mem::size_of::<i64>() as isize;
        ptr::write(dirp.offset(j).cast::<u16>(), ent.reclen as u16);
        j += std::mem::size_of::<u16>() as isize;
        ptr::write(dirp.offset(j).cast::<u8>(), ent.ty);
        j += std::mem::size_of::<u8>() as isize;
        let reclen = ent.reclen;
        ptr::copy_nonoverlapping(
            ent.name.as_ptr() as *const u8,
            dirp.offset(j),
            ent.name.as_bytes().len(),
        );
        k += reclen as isize;
        i += 1;
    }
    dents.len()
}

pub unsafe fn deserialize_dirents(bytes: &[u8]) -> Vec<Dirent64> {
    let mut res = Vec::new();

    let dirp: *const u8 = bytes.as_ptr();
    let nb = bytes.len();
    let mut k = 0;

    while k < nb as isize {
        let dirp = dirp.offset(k);
        let mut j: isize = 0;
        let ino = ptr::read(dirp.offset(j).cast::<u64>());
        j += std::mem::size_of::<u64>() as isize;
        let off = ptr::read(dirp.offset(j).cast::<i64>());
        j += std::mem::size_of::<i64>() as isize;
        let reclen = ptr::read(dirp.offset(j).cast::<u16>());
        j += std::mem::size_of::<u16>() as isize;
        let name = std::slice::from_raw_parts(dirp.offset(j), reclen as usize - j as usize);
        let name = Vec::from(name);
        j = reclen as isize - 1;
        let ty = ptr::read(dirp.offset(j).cast::<u8>());
        let ent = Dirent64 {
            ino,
            off,
            reclen,
            ty,
            name: CString::from_vec_with_nul_unchecked(name),
        };
        res.push(ent);
        k += reclen as isize;
    }
    res
}

